static char generate_random_char() {
    uint8_t random_num = esp_random() % 62;

    if (random_num < 10) {
        return (char)('0' + random_num);
    }
    else if (random_num < 36) {
        return (char)('A' + (random_num - 10));
    }
    else {
        return (char)('a' + (random_num - 36));
    }
}


void htool_ble_set_adv_data(adv_t index) {
    if (index == ADV_RANDOM) {
        index = esp_random() % ADV_RANDOM;
    }
    if (index == ADV_APPLE_RANDOM) {
        index = esp_random() % ADV_APPLE_TVCOLORBALANCE;
    }
    else if (index == ADV_GOOGLE) {
        google_adv[13] = (esp_random() % 120) - 100;
    }
    else if (index == ADV_SAMSUNG_RANDOM) {
        samsung_adv[14] = (esp_random() % 32) + 1;
    }
    else if (index == ADV_SAMSUNG_WATCH4) {
        samsung_adv[14] = 0x04;
    }
    else if (index == ADV_SAMSUNG_FRENCH_WATCH4) {
        samsung_adv[14] = 0x0A;
    }
    else if (index == ADV_SAMSUNG_FOX_WATCH5) {
        samsung_adv[14] = 0x0C;
    }
    else if (index == ADV_SAMSUNG_WATCH5) {
        samsung_adv[14] = 0x11;
    }
    else if (index == ADV_SAMSUNG_WATCH5_PRO) {
        samsung_adv[14] = 0x15;
    }
    else if (index == ADV_SAMSUNG_WATCH6) {
        samsung_adv[14] = 0x1E;
    }
    else if (index == ADV_MICROSOFT) {
        microsoft_adv[7] = generate_random_char();
        microsoft_adv[8] = generate_random_char();
        microsoft_adv[9] = generate_random_char();
        microsoft_adv[10] = generate_random_char();
        microsoft_adv[11] = generate_random_char();
        microsoft_adv[12] = generate_random_char();
        microsoft_adv[13] = generate_random_char();
        microsoft_adv[14] = generate_random_char();
    }
    else {
        index--;
    }
    if (index == ADV_GOOGLE) {
        esp_ble_gap_config_adv_data_raw((uint8_t *)google_adv, 14);
    }
    else if (index >= ADV_SAMSUNG_RANDOM && index <= ADV_SAMSUNG_WATCH6) {
        esp_ble_gap_config_adv_data_raw((uint8_t *)samsung_adv, 15);
    }
    else if (index == ADV_MICROSOFT) {
        esp_ble_gap_config_adv_data_raw((uint8_t *)microsoft_adv, 15);
    }
    else {
        esp_ble_gap_config_adv_data_raw((uint8_t *)apple_adv[index], index > 16 ? 23 : 31);
    }
}


void htool_ble_start_adv() {
    esp_ble_gap_start_advertising((esp_ble_adv_params_t *)&adv_params);
    ble_adv_running = true;
}


void htool_ble_stop_adv() {
    esp_ble_gap_stop_advertising();
    ble_adv_running = false;
}


uint8_t htool_ble_deinit() {
    if (ble_init) {
        if (esp_bluedroid_disable() != ESP_OK) {
            ESP_LOGE(TAG, "Failed to disable bluedroid");
            return HTOOL_BLE_ERR;
        }
        if (esp_bluedroid_deinit() != ESP_OK) {
            ESP_LOGE(TAG, "Failed to deinitialize bluedroid");
            return HTOOL_BLE_ERR;
        }
        if (esp_bt_controller_disable() != ESP_OK) {
            ESP_LOGE(TAG, "Failed to disable BLE controller");
            return HTOOL_BLE_ERR;
        }
        if (esp_bt_controller_deinit() != ESP_OK) {
            ESP_LOGE(TAG, "Failed to deinitialize BLE controller");
            return HTOOL_BLE_ERR;
        }
        ble_init = false;
    }

    return HTOOL_BLE_OK;
}


uint8_t htool_ble_init() {
    if (!ble_init) {
        esp_bt_controller_config_t bt_cfg = BT_CONTROLLER_INIT_CONFIG_DEFAULT();
        if (esp_bt_controller_init(&bt_cfg) != ESP_OK) {
            ESP_LOGE(TAG, "initialize bt controller failed");
            return HTOOL_BLE_ERR;
        }
        if (esp_bt_controller_enable(ESP_BT_MODE_BLE) != ESP_OK) {
            ESP_LOGE(TAG, "enable ble controller failed");
            return HTOOL_BLE_ERR;
        }
        if (esp_bluedroid_init() != ESP_OK) {
            ESP_LOGE(TAG, "init bluedroid failed");
            return HTOOL_BLE_ERR;
        }
        if (esp_bluedroid_enable() != ESP_OK) {
            ESP_LOGE(TAG, "bluedroid enable failed");
            return HTOOL_BLE_ERR;
        }
        if (esp_ble_tx_power_set(ESP_BLE_PWR_TYPE_ADV, ESP_PWR_LVL_P9) != ESP_OK) {
            ESP_LOGE(TAG, "error setting ble tx power");
            return HTOOL_BLE_ERR;
        }
        ble_init = true;
    }

    return HTOOL_BLE_OK;
}
